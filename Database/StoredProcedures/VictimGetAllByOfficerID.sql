CREATE PROCEDURE [VictimGetAllByOfficerID]

@OfficerID	INT

AS

BEGIN

	SET NOCOUNT ON;

	SELECT 
		o.OffenderID, 
		ISNULL(o.FirstName, '') + ' ' +
		ISNULL(o.MiddleName, '') + ' ' +
		ISNULL(o.LastName, '') AS 'OffenderName'
	FROM 
		Offender_Officer oo
		JOIN Offender o ON oo.OffenderID = o.OffenderID
	WHERE 
		(oo.OfficerID = @OfficerID)
		AND 
		(o.Deleted = 0)
		AND
		(o.Victim = 1)  

END
