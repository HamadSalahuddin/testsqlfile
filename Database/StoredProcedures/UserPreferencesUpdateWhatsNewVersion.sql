CREATE PROCEDURE [UserPreferencesUpdateWhatsNewVersion]    
    @UserID int,    
    @WhatsNewVersion varchar(50)    
AS    
BEGIN    
    SET NOCOUNT ON;    
    IF (SELECT Count(*) FROM dbo.UserPreferences WHERE UserID = @UserID) > 0    
        BEGIN    
            UPDATE dbo.UserPreferences    
                SET whatsNewVersion = @WhatsNewVersion    
                WHERE UserID = @UserID    
        END  
END  

