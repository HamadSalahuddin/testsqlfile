CREATE PROCEDURE [spReportAgencyOfficerContactDetail]

AS
Select 
Ag.Agency
,ag.EmailAddress As 'Agency Email'
,ag.OnCallEmail As 'AgencyOnCallEmail'
,ag.Phone As 'AgencyPhone'
,ofi.FirstName+' '+ofi.LastName As 'OfficerName'
,ofi.DayPhone As 'Officer Phone'
,ofi.MobilePhone
,ofi.EmailAddress1
,ofi.EmailAddress2
,ofi.deleted
From Officer ofi
JOIN Agency ag ON ag.agencyid = ofi.agencyid

