CREATE PROCEDURE [BeaconGetByID]

@BeaconID INT

AS

	SET NOCOUNT ON

	SELECT	b.BeaconName,b.Identifier,b.AddressID,
            bo.OffenderID,obt.BeaconThresholdID,
            a.Street1,a.Street2,a.ZipCode,a.City,a.StateID,
            g.Longitude,g.Latitude

	FROM	Beacon b
	inner join BeaconOffender bo ON b.ID = bo.BeaconID 
	left join OffenderBeaconThreshold obt ON bo.OffenderID = obt.offenderID 
	inner join Address a ON a.ID = b.AddressID 	
    inner join GPSLocation g ON g.AddressID = a.ID

    WHERE  b.ID = @BeaconID




GO
