CREATE PROCEDURE [AgencyServiceAttributesGet] @AgencyID INT, @ServiceID INT  
AS  
--Since the services attributes are in xml format, this procedure will return them in dataset format  
--so as to reduce the amount of processing on the business layer.  
  
DECLARE @hDoc INT  
DECLARE @Xml XML  
  
  
SET @Xml = (SELECT ServiceAttributes FROM AgencyServices WHERE AgencyID = @AgencyID AND ServiceID = @ServiceID)  
  
EXEC sp_xml_preparedocument @hDoc OUTPUT, @Xml  
  
SELECT @AgencyID AS AgencyID, @ServiceID AS ServiceID,  
  * FROM OPENXML(@hDoc,'/ServiceAttributes/Attribute',2)  
      WITH(AttributeName VARCHAR(MAX) './@Name',  
     DataType VARCHAR(35),  
     AttributeValue VARCHAR(MAX))  
  
EXEC sp_xml_removedocument @hDoc  
  
  
  
  
GO
GRANT EXECUTE ON [AgencyServiceAttributesGet] TO [db_dml]
GO
