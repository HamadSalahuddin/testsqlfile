CREATE PROCEDURE [AgencyServicesGet] @AgencyID INT  
AS  
  
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED;  
  
SELECT a.AgencyID, a.Agency, s.ServiceName  
FROM Agency a INNER JOIN AgencyServices asr ON a.AgencyID = asr.AgencyID  
INNER JOIN Services s ON asr.ServiceID = s.ServiceID  
WHERE a.AgencyID = @AgencyID  
GO
